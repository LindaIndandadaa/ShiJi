package entity;

public class Detective { 
	    private String name;
	    private String intro;

	    public String getDetectivename() {
	        return name;
	    }
	    public void setDetectivename(String username) {
	        this.name = username;
	    }
	    public String getDetectiveintro() {
	        return intro;
	    }
	    public void setDetectiveintro(String intro) {
	        this.intro = intro;
	    }
}
